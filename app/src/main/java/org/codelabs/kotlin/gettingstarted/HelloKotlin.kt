package org.codelabs.kotlin.gettingstarted


/**
 * Method showcasing how to use print values in Kotlin using print() and println()
 */
fun printHello() {
    //This will print the passed in the same line
    print("Hello World")
    //This will print the passed value in new line
    println("Hello World")
}


//Main method to call the above method
fun main() {
    printHello()
}

